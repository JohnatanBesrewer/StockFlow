import functools
import sqlite3

# ==============================================================================
# R E P O S I T O R Y   E R R O R S
# Возникают на уровне доступа к данным. Не должны всплывать выше сервиса.
# ==============================================================================


class RepositoryError(Exception):
    """Базовый класс для всего, что идёт снизу — из инфраструктурного слоя.
    Выбрасывается декоратором @handle_db_errors при любой необработанной sqlite3.Error.
    Сервис может ловить его как "catch-all" для ситуаций, когда важно знать,
    что что-то пошло не так с хранилищем, но конкретика не важна.
    Никогда не должен всплывать выше сервисного слоя.
    """


class ConstraintViolationError(RepositoryError):
    """Выбрасывается при нарушении ограничений базы данных: UNIQUE, FOREIGN KEY, CHECK.
    Происходит в момент вызова cursor.execute(...) внутри репозитория.
    Оборачивает sqlite3.IntegrityError через from e, поэтому первопричина всегда доступна через e.__cause__.
    Сервис ловит его и маппит в конкретную бизнес-ошибку, анализируя текст __cause__.
    """


class DataIntegrityError(RepositoryError):
    """Выбрасывается вручную внутри репозитория, когда данные технически есть в БД,
    но находятся в невалидном состоянии с точки зрения приложения.
    Примеры из твоего кода: _build_product_from_row вернул строку без активной цены,
    set_sale_price не нашёл товар по id.
    """


# ==============================================================================
# S E R V I C E   E R R O R S
# Ошибки бизнес-логики. Именно их ловит вызывающий код (UI, CLI, API).
# ==============================================================================


class ServiceError(Exception):
    """Базовый класс бизнес-ошибок.
    Именно этот тип (и его потомков) должен ловить вызывающий код — UI, CLI или API-обработчик.
    Выше этого уровня RepositoryError не должен попадать никогда.
    Если это произошло — значит, в сервисе пропущен except.
    """


class NotFoundError(ServiceError):
    """Запрошенный ресурс не существует.
    Выбрасывается сервисом, когда репозиторий вернул None.
    Соответствует HTTP 404. Полезно иметь как отдельный базовый класс,
    чтобы UI мог показывать "не найдено" без знания конкретного типа сущности.
    """


class ProductNotFoundError(NotFoundError):
    """Конкретизация NotFoundError для товаров.
    Выбрасывается перед любой операцией, которая требует существующего товара:
    delete_product, update_product, set_sale_price на уровне сервиса.
    Позволяет вызывающему коду реагировать точечно именно на отсутствие товара.
    """


class ConflictError(ServiceError):
    """Операция невозможна из-за конфликта с уже существующими данными.
    Соответствует HTTP 409. Выбрасывается сервисом при маппинге ConstraintViolationError.
    Базовый класс позволяет UI показать обобщённое "уже существует" без разбора деталей.
    """


class DuplicateBarcodeError(ConflictError):
    """Штрихкод уже привязан к другому товару.
    Выбрасывается при create_product или update_product,
    когда ConstraintViolationError.__cause__ указывает на таблицу barcodes.
    Содержит в сообщении конкретный штрихкод — удобно для отображения пользователю.
    """


class DuplicateProductNameError(ConflictError):
    """Товар с таким именем уже существует.
    Аналогично, но для нарушения UNIQUE на поле products.name.
    Отдельный класс нужен, чтобы форма могла подсветить именно поле "название",
    а не поле "штрихкод".
    """


# ==============================================================================
# D E C O R A T O R
# ==============================================================================


def handle_db_errors(func):
    @functools.wraps(func)
    def wrapper(*args, **kwargs):
        try:
            return func(*args, **kwargs)

        # наши ошибки не трогаем
        except RepositoryError:
            raise

        # ошибки ограничений (UNIQUE, FK, CHECK)
        except sqlite3.IntegrityError as e:
            raise ConstraintViolationError("Нарушены ограничения БД") from e

        # все остальные ошибки SQLite
        except sqlite3.Error as e:
            raise RepositoryError("Ошибка доступа к данным") from e

    return wrapper


